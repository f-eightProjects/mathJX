package com.mathjx.basic;

public class Subtraction {

    public static int sub(int a, int b) {

        return a - b;
    }

    public static double sub(double a, double b) {
        return a - b;
    }

    public static float sub(float a, float b) {
        return a - b;
    }

    public static long sub(long a, long b) {
        return a - b;
    }
}
